                const counterElement = $('#' + counter.id);

                const step = (timestamp) => {
                if (!startTimestamp) startTimestamp = timestamp;
                const progress = timestamp - startTimestamp;
                const percentage = Math.min(progress / counter.duration, 1);
                const count = Math.round(percentage * counter.end);
                counterElement.text(count.toLocaleString('en', { maximumFractionDigits: 0 }));

                if (progress < counter.duration) {
                    requestAnimationFrame(step);
                }
                };

                requestAnimationFrame(step);
            });

            function restartMarquee() {
                $('.marquee-container').css('animation', 'none').height(); // Reset animation
